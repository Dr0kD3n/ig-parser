#!/bin/bash

# Переходим в директорию, где лежит сам скрипт
cd "$(dirname "$0")"

echo "=========================================================="
echo "  ЗАПУСК БОТА (Локальные модули в SRC)"
echo "=========================================================="

# 1. Проверяем наличие папки src
if [ ! -d "src" ]; then
    echo "[ОШИБКА] Папка 'src' не найдена!"
    read -p "Нажмите Enter, чтобы выйти..."
    exit 1
fi

# 2. ПЕРЕХОДИМ В SRC
cd src

# 3. Установка зависимостей (ПРЯМО В ПАПКУ SRC)
if [ ! -f "package.json" ]; then
    echo "[ВНИМАНИЕ] Файл package.json должен лежать внутри папки src!"
    echo "Перенесите его из корня в папку src и перезапустите скрипт."
    read -p "Нажмите Enter, чтобы выйти..."
    exit 1
fi

if [ ! -d "node_modules" ]; then
    echo "[1/2] Установка модулей в папку src..."
    npm install
else
    echo "[1/2] Модули уже установлены!"
fi

# 4. Установка браузеров
echo "[2/2] Проверка Playwright..."
npx playwright install chromium

# 5. ЗАПУСК
echo "----------------------------------------------------------"
echo "[!] Готово! Запускаем бота..."

# Запускаем сервер (или index.js, в зависимости от твоего файла)
if [ -f "server.js" ]; then
    node server.js
else
    node index.js
fi

# Проверка кода завершения
if [ $? -ne 0 ]; then
    echo ""
    echo "[!] Произошла ошибка. Проверьте логи выше."
fi

read -p "Работа завершена. Нажмите Enter для выхода..."